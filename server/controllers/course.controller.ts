//upload course

export const uploadCourse = CatchAsyncError(
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const data = req.body;
      const thumnnail = data.thumnnail;
      if (thumnnail) {
        const myCloud = await cloudinary.v2.uploader.upload(thumnnail, {
          folder: "courses",
        });
        data.thumnnail = {
          public_id: myCloud.public_id,
          url: myCloud.secure_url,
        };
      }

      createCourse(data, res, next);
    } catch (error: any) {
      return next(new ErrorHandler(error.message, 500));
    }
  }
);

export const editCourse = CatchAsyncError(
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const data = req.body;
      const thumnnail = data.thumnnail;

      if (thumnnail) {
        await cloudinary.v2.uploader.destroy(thumnnail.public_id);

        const myCloud = await cloudinary.v2.uploader.upload(thumnnail, {
          folder: "courses",
        });
        data.thumnnail = {
          public_id: myCloud.public_id,
          url: myCloud.secure_url,
        };

        const courseId = req.params.id;
        const course = await CourseModel.findByIdAndUpdate(
          courseId,
          {
            $set: data,
          },
          { new: true }
        );

        res.status(201).json({
          success: true,
          course,
        });
      }
    } catch (error: any) {
      return next(new ErrorHandler(error.message, 500));
    }
  }
);

//get single course -- without purchase

export const getSingleCourse = CatchAsyncError(
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const courseId = req.params.id;
      const isCacheExist = await redis.get(courseId);

      if (isCacheExist) {
        const course = JSON.parse(isCacheExist);
        res.status(200).json({
          success: true,
          course,
        });
      } else {
        const course = await CourseModel.findById(req.params.id).select(
          "-courseData.suggestion -courseData.questions -courseData.links"
        );

        if (!course) {
          return next(new ErrorHandler("Course not found", 404));
        }

        await redis.set(courseId, JSON.stringify(course));
        res.status(200).json({
          success: true,
          course,
        });
      }
    } catch (error: any) {
      return next(new ErrorHandler(error.message, 500));
    }
  }
);

